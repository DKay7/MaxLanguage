Opcode *MachineOpcodes::GetOpcodeById (InstructionId id) {

    if (id == InstructionId::NO_ID)
        return nullptr;

    return &idToOpcode [id];
}

//------------------------------------------------------------------------------------------------------------------------------
//-------------------------------------------------Value------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------------------------------

x86Opcodes::x86Opcodes () {
    idToOpcode.insert ({InstructionId::ADD, {}});
}
